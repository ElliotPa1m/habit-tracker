export function checkAchievements(habits, allCompletions, unlockedKeys) {
    const toUnlock = []
    const addIfNew = (key) => { if (!unlockedKeys.includes(key)) toUnlock.push(key) }

    // General
    if (habits.length >= 1) addIfNew('first_habit')
    if (habits.length >= 3) addIfNew('habits_3')
    if (habits.length >= 5) addIfNew('habits_5')
    if (habits.length >= 10) addIfNew('habits_10')

    // Completions
    if (allCompletions.length >= 10) addIfNew('completions_10')
    if (allCompletions.length >= 25) addIfNew('completions_25')
    if (allCompletions.length >= 50) addIfNew('completions_50')
    if (allCompletions.length >= 100) addIfNew('completions_100')

    // Category counts
    const categoryCounts = {}
    for (const habit of habits) {
        categoryCounts[habit.category] = (categoryCounts[habit.category] || 0) + 1
    }

    const categoryKeys = ['physical', 'health', 'hygiene', 'fun', 'relationships', 'emotional', 'time', 'economics']
    for (const cat of categoryKeys) {
        const count = categoryCounts[cat] || 0
        if (count >= 1) addIfNew(`${cat}_habit`)
        if (count >= 3) addIfNew(`${cat}_3`)
        if (count >= 5) addIfNew(`${cat}_5`)
        if (count >= 10) addIfNew(`${cat}_10`)
    }

    // Streaks
    for (const habit of habits) {
        const dates = allCompletions
            .filter(c => c.habit_id === habit.id)
            .map(c => c.completed_date)

        let streak = 0
        const today = new Date()
        for (let i = 1; i <= 365; i++) {
            const date = new Date(today)
            date.setDate(today.getDate() - i)
            const dateStr = date.toISOString().split('T')[0]
            if (dates.includes(dateStr)) streak++
            else break
        }

        if (streak >= 3) addIfNew('streak_3')
        if (streak >= 7) addIfNew('streak_7')
        if (streak >= 14) addIfNew('streak_14')
        if (streak >= 30) addIfNew('streak_30')
    }

    // Perfect day
    const today = new Date().toISOString().split('T')[0]
    const completedToday = allCompletions.filter(c => c.completed_date === today).map(c => c.habit_id)
    if (habits.length > 0 && habits.every(h => completedToday.includes(h.id))) {
        addIfNew('perfect_day')
    }

    // Perfect week
    if (habits.length > 0) {
        let perfectWeek = true
        for (let i = 0; i < 7; i++) {
            const date = new Date()
            date.setDate(date.getDate() - i)
            const dateStr = date.toISOString().split('T')[0]
            const dayCompletions = allCompletions.filter(c => c.completed_date === dateStr).map(c => c.habit_id)
            if (!habits.every(h => dayCompletions.includes(h.id))) {
                perfectWeek = false
                break
            }
        }
        if (perfectWeek) addIfNew('perfect_week')
    }

    // Perfect month
    if (habits.length > 0) {
        let perfectMonth = true
        for (let i = 0; i < 30; i++) {
            const date = new Date()
            date.setDate(date.getDate() - i)
            const dateStr = date.toISOString().split('T')[0]
            const dayCompletions = allCompletions.filter(c => c.completed_date === dateStr).map(c => c.habit_id)
            if (!habits.every(h => dayCompletions.includes(h.id))) {
                perfectMonth = false
                break
            }
        }
        if (perfectMonth) addIfNew('perfect_month')
    }

    // No miss — aldrig missat en dag sedan första habit skapades
    if (habits.length > 0) {
        const firstHabitDate = habits
            .map(h => h.created_at.split('T')[0])
            .sort()[0]

        const start = new Date(firstHabitDate)
        const today = new Date()
        const diffDays = Math.floor((today - start) / (1000 * 60 * 60 * 24))

        let noMiss = true
        for (let i = 0; i <= diffDays; i++) {
            const date = new Date(start)
            date.setDate(start.getDate() + i)
            const dateStr = date.toISOString().split('T')[0]
            const habitsAtDate = habits.filter(h => h.created_at.split('T')[0] <= dateStr)
            if (habitsAtDate.length === 0) continue
            const dayCompletions = allCompletions.filter(c => c.completed_date === dateStr).map(c => c.habit_id)
            if (!habitsAtDate.every(h => dayCompletions.includes(h.id))) {
                noMiss = false
                break
            }
        }
        if (noMiss && diffDays > 0) addIfNew('no_miss')
    }

    return toUnlock
}